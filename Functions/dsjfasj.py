# Normal multiplication
res1 = 5 * 7
print("Result 1: ", res1)
# normal division - autom. float
res2 = 10/3
res3 = 8/2
print("Result 2: ",res2, "Result 3: ",res3)
print(type(res2), type(res3))
# % modulo
res4 = 7%3
print("Result 4: ",res4)
# floor division - rounds to the next smallest whole number
res5 = 8 // 3
res6 = -8 // 3
print("Result 5: ",res5, ", Result 6: ", res6)
# exponentiation
res7 = 5**3
print("Result 7: ",res7)
# attention: exponential calc. comes before mult. and div.
res8 = 5 + 2 * 3**3
print("Result 8: ",res8)


